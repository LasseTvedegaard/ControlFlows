namespace ControlFlow2 {
    public class ControlFlow2 {
        static void Main(string[] args) {

            int[] numbers = { 1, 982, 343, 63, 81, 90, 4, 62, 75, 245 };

            for (int I = 0; I < numbers.Length; I++) {
                Console.WriteLine(numbers[I]);
            }
        }
    }
}